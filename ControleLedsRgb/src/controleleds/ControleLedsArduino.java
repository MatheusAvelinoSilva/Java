    private void jSpinnerBrilhoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerBrilhoStateChanged

        brilho = (int) jSpinnerBrilho.getValue();
        jSliderBrilho.setValue(brilho);

        if (brilho < 0) {

            JOptionPane.showMessageDialog(rootPane, "Valor Minimo");
            jSpinnerBrilho.setValue(0);

        } else if (brilho < 9) {

            valorBrilho = "00" + brilho;

        } else if (brilho < 99) {

            valorBrilho = "0" + brilho;

        } else if (brilho <= 255) {

            valorBrilho = "" + brilho;

        } else if (brilho > 255) {

            JOptionPane.showMessageDialog(rootPane, "Valor Maximo");
            jSpinnerBrilho.setValue(255);

        }

    }//GEN-LAST:event_jSpinnerBrilhoStateChanged

