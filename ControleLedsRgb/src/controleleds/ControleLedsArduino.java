    private void jButtonConcatenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConcatenarActionPerformed

        String textoComando = jTextFieldComando.getText();
        int tamanhoComando = textoComando.length();

        comando = valorCustomizacao + " " + valorEfeito + " " + valorBrilho + " " + valorTempo + " " + valorVermelho + "." + valorVerde + "." + valorAzul;

        if (textoComando.contains(valorVermelho + "." + valorVerde + "." + valorAzul)) {

            JOptionPane.showMessageDialog(rootPane, "Valor Igual");

        } else {

            if (tamanhoComando == 0) {

                jTextFieldComando.setText(comando);

            } else if (tamanhoComando >= 24 && tamanhoComando <= 72) {

                jTextFieldComando.setText(textoComando + " " + valorVermelho + "." + valorVerde + "." + valorAzul);

            }
        }

    }//GEN-LAST:event_jButtonConcatenarActionPerformed

    private void jButtonSubstituirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubstituirActionPerformed

        try {

            String comandoTexto = jTextFieldComando.getText();
            int opcao = Integer.parseInt(JOptionPane.showInputDialog("Digite Valor: "));

            if (comandoTexto.contains(valorCustomizacao + " " + valorEfeito + " " + valorBrilho + " " + valorTempo) && comandoTexto.contains(valorVermelho + "." + valorVerde + "." + valorAzul)) {

                JOptionPane.showMessageDialog(rootPane, "Comando Igual");

            } else {

                if (opcao == 0) {

                    comando = comando.replace(comando.substring(opcao, opcao + 12), valorCustomizacao + " " + valorEfeito + " " + valorBrilho + " " + valorTempo);
                    jTextFieldComando.setText(comando);

                } else {

                    comando = comandoTexto.replace(comandoTexto.substring(opcao, opcao + 12), "/");
                    comando = comando.replace("/", " " + valorVermelho + "." + valorVerde + "." + valorAzul);

                    jTextFieldComando.setText(comando);

                }
            }

        } catch (NumberFormatException | StringIndexOutOfBoundsException e) {

            JOptionPane.showMessageDialog(rootPane, "Digite Um Valor Valido");

        }

    }//GEN-LAST:event_jButtonSubstituirActionPerformed

